#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	unsigned int	i;
	unsigned int	n;

	i = 0;
	n = ft_strlen(s1);
	while (s2[i])
	{
		s1[n] = s2[i];
		i++;
		n++;
	}
	s1[n] = '\0';
	return (s1);
}
